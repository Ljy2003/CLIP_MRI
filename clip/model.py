class CLIP(nn.Module):
    def __init__(self,
                 args,
                 embed_dim: int,
                 # vision
                 vision_model,
                 # text
                 transformer_model,
                 transformer_width
                 ):
        super().__init__()

        self.transformer = transformer_model
        self.visual = vision_model
        self.device = args.device

        self.text_projection = nn.Parameter(
            torch.randn(transformer_width, embed_dim))
        self.logit_scale = nn.Parameter(torch.ones([]) * np.log(1 / 0.07))

    def build_attention_mask(self):
        # lazily create causal attention mask, with full attention between the vision tokens
        # pytorch uses additive attention mask; fill with -inf
        mask = torch.empty(self.context_length, self.context_length)
        mask.fill_(float("-inf"))
        mask.triu_(1)  # zero out the lower diagonal
        return mask

    @property
    def dtype(self):
        return torch.float32

    def encode_image(self, image):
        return self.visual(image.type(self.dtype))

    def encode_text(self, texts):
        output = torch.tensor(self.transformer.encode(texts).astype('float32'))
        output = output.to(self.device)
        # x.shape = [batch_size, n_ctx, transformer.width]
        # take features from the eot embedding (eot_token is the highest number in each sequence)

        output = output[torch.arange(output.shape[0])] @ self.text_projection
        return output

    def freeze_model(self):
        pass

    def forward(self, image, text):
        image_features = self.encode_image(image)
        text_features = self.encode_text(text)

        # normalized features
        image_features = image_features / \
            image_features.norm(dim=1, keepdim=True)
        text_features = text_features / text_features.norm(dim=1, keepdim=True)

        # cosine similarity as logits
        logit_scale = self.logit_scale.exp()
        logits_per_image = logit_scale * image_features @ text_features.t()
        logits_per_text = logits_per_image.t()

        # shape = [global_batch_size, global_batch_size]
        return logits_per_image, logits_per_text


def build_model(args):
    transformer = FlagModel(args.transformer_name)
    if args.visual_model == 'Resnet_101':
        vision_model = ResNet_101(output_dim=args.embed_dim)
    elif args.visual_model == 'Resnet_50':
        vision_model = ResNet_50(output_dim=args.embed_dim)
    else:
        raise Exception('no model named '+args.visual_model)
    model = CLIP(args=args, embed_dim=args.embed_dim, vision_model=vision_model, transformer_model=transformer,
                 transformer_width=args.transformer_width)

    return model
