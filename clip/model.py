class ResNet_101(nn.Module):
    def __init__(self, output_dim):
        super().__init__()
        self.resnet = torchvision.models.resnet101()
        self.relu = nn.ReLU()
        self.fc = nn.Linear(in_features=1000, out_features=output_dim)

    def freeze_model(self):
        for param in self.resnet.parameters():
            param.requires_grad = False

    def forward(self, x):
        x = self.relu(self.resnet(x))
        return self.fc(x)


